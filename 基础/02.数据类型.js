// string 类型
// 在js 中包含 '' "" `` 三种字符串方式。

let name = 'john'
console.log(`hello, ${name}!`); // hello john!

console.log(`this result is ${1+2}`); // result is 3

// boolean 类型 逻辑类型 只包含 true false
// 布尔值 也可以做比较

let isGraeter = 4 > 1;
console.log(isGraeter);


// null 值
// null 值不属于任何一种类型 是独立的类型 只包含null只代表 无 空 值未知
let age = null
console.log(age);

// undefined 值
// undefined 的 含义是未被赋值。
// 如果一个哪里被声明，但未被复制，那么它的值就是 undefined

